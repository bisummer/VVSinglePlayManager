package movie.example.ls.vvmoviemanager.bean;

/**
 * C 小表情
 */
public class SmailSmallBean {

    private String spannable;
    private String string;

    public SmailSmallBean(String spannable, String string) {
        this.spannable = spannable;
        this.string = string;
    }

    public String getSpannable() {
        return spannable;
    }

    public void setSpannable(String spannable) {
        this.spannable = spannable;
    }

    public String getString() {
        return string;
    }

    public void setString(String string) {
        this.string = string;
    }
}
